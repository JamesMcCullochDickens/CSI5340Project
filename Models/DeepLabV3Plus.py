class DeepLabHeadV3Plus(nn.Module):
    def __init__(self, backbone=None, in_channels=2048, low_level_channels=256, num_classes=37, aspp_dilate=[12, 24, 36], loss="ce"):
        super(DeepLabHeadV3Plus, self).__init__()

        if backbone is None:
            #rn_101 = ResNet.remove_head(ResNet.resnet101(pretrained=True, dilation_vals=[False, True, True]))
            # self.backbone = ResNet.DeepLabV3PlusBackbone(backbone)
            self.backbone = SegModels.deeplabv3_resnet101(pretrained=True).backbone
        else:
            self.backbone = backbone
        self.project = nn.Sequential(
            nn.Conv2d(low_level_channels, 48, 1, bias=False),
            nn.BatchNorm2d(48),
            nn.ReLU(inplace=True),
        )

        self.aspp = ASPP(in_channels, aspp_dilate)

        classifier = nn.Sequential(
            nn.Conv2d(304, 256, 3, padding=1, bias=False),
            nn.BatchNorm2d(256),
            nn.ReLU(inplace=True),
            nn.Conv2d(256, num_classes, 1)
        )
        #self._init_weight()
        if loss == "ce":
            self.loss = nn.CrossEntropyLoss(ignore_index=0)
        elif loss == "focal_loss":
            self.loss = losses.FocalLoss()
            pass

        self.classifier = convert_to_separable_conv(classifier)

    def forward(self, ims, gt=None, hw_tuple=None):
        im_height = ims.shape[2]
        im_width = ims.shape[3]
        feature = self.backbone(ims)
        low_level_feature = self.project(feature['low_level'])
        output_feature = self.aspp(feature['out'])
        output_feature = F.interpolate(output_feature, size=low_level_feature.shape[2:], mode='bilinear',
                                       align_corners=False)
        output = self.classifier(torch.cat([low_level_feature, output_feature], dim=1))
        if hw_tuple is None:
            output = F.interpolate(output, (im_height, im_width), mode='bilinear',
                                           align_corners=False)
        else:
            output = F.interpolate(output, hw_tuple, mode='bilinear',
                                   align_corners=False)

        if self.training:
            loss = self.loss(output, gt)
        else:
            loss = None
            output = torch.argmax(output, dim=1)
        return output, loss

    def _init_weight(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight)
            elif isinstance(m, (nn.BatchNorm2d, nn.GroupNorm)):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)


